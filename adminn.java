package projet_java;



public class adminn {
    private String card_number_admin;
    private String pin_admin;
    
    public adminn(String card_number_admin, String pin_admin) {
        this.card_number_admin = card_number_admin;
        this.pin_admin = pin_admin;
    }
	public String getCard_number_admin() {
		return card_number_admin;
	}
	public void setCard_number_admin(String card_number_admin) {
		this.card_number_admin = card_number_admin;
	}
	public String getPin_admin() {
		return pin_admin;
	}
	public void setPin_admin(String pin_admin) {
		this.pin_admin = pin_admin;
	}

}


 
    
